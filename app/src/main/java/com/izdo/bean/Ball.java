public class Ball {

    private String color;
    private int colorImage;
    private String colorText;
    private int colorCheckedImage;

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }

    public int getColorImage() {
        return colorImage;
    }

    public void setColorImage(int colorImage) {
        this.colorImage = colorImage;
    }

    public String getColorText() {
        return colorText;
    }

    public void setColorText(String colorText) {
        this.colorText = colorText;
    }

    public int getColorCheckedImage() {
        return colorCheckedImage;
    }

    public void setColorCheckedImage(int colorCheckedImage) {
        this.colorCheckedImage = colorCheckedImage;
    }
}
